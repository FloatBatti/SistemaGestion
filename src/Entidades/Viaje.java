package Entidades;


public class Viaje {
    
    private int IdViaje;
    private float Estimado;
    private float ResultadoBruto;
    private float KilosVendidos;
    private float OcupacionBodegas;
    private float SumasVentas;
    private float RentabilidadBruta;

    public Viaje() {
    }

    public int getIdViaje() {
        return IdViaje;
    }

    public void setIdViaje(int IdViaje) {
        this.IdViaje = IdViaje;
    }

    public float getEstimado() {
        return Estimado;
    }

    public void setEstimado(float Estimado) {
        this.Estimado = Estimado;
    }

    public float getResultadoBruto() {
        return ResultadoBruto;
    }

    public void setResultadoBruto(float ResultadoBruto) {
        this.ResultadoBruto = ResultadoBruto;
    }

    public float getKilosVendidos() {
        return KilosVendidos;
    }

    public void setKilosVendidos(float KilosVendidos) {
        this.KilosVendidos = KilosVendidos;
    }

    public float getOcupacionBodegas() {
        return OcupacionBodegas;
    }

    public void setOcupacionBodegas(float OcupacionBodegas) {
        this.OcupacionBodegas = OcupacionBodegas;
    }

    public float getSumasVentas() {
        return SumasVentas;
    }

    public void setSumasVentas(float SumasVentas) {
        this.SumasVentas = SumasVentas;
    }

    public float getRentabilidadBruta() {
        return RentabilidadBruta;
    }

    public void setRentabilidadBruta(float RentabilidadBruta) {
        this.RentabilidadBruta = RentabilidadBruta;
    }
   
   
   
   
           
}
